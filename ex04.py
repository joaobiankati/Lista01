comprimento = float(input("Informe o comprimento da caixa retangular: "))
largura = float(input("Informe a largura da caixa retangular: "))
volume = float(input("Informe o volume da caixa retangular: "))

calculo = comprimento * largura * volume

print(f"A área total da caixa retangular é {calculo}")